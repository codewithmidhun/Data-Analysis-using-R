# Variables of different types 
# Real Numeric 
myRealNumeric <- 10 
class(myRealNumeric) 
# Decimal Numeric 
myDecimalNumeric <- 10.0 
class(myDecimalNumeric) 
# Character 
myCharacter <- "10" 
class(myCharacter) 
# Logical 
myBoolean <- TRUE 
class(myBoolean) 
# Integer 
myInteger <- 0:10 
class(myInteger) 
#Complex 
myComplex <- 5i 
class(myComplex)
